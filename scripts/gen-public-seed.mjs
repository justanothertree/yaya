// Generates src/circuit/publicSeed.ts — the Evan-only (pid '2') slice of the Circuit —
// from the full src/circuit/seed.ts, WITHOUT the output importing seed.ts. This keeps
// the rest of the group's real data out of the client bundle (seed.ts is imported by
// nothing in the app and so is tree-shaken away). Re-run after editing seed.ts:
//   node scripts/gen-public-seed.mjs
import { transform } from 'esbuild'
import { readFileSync, writeFileSync } from 'node:fs'

const EVAN = '2'

const tsSource = readFileSync('src/circuit/seed.ts', 'utf8')
const { code } = await transform(tsSource, { loader: 'ts', format: 'esm' })
const mod = await import('data:text/javascript;base64,' + Buffer.from(code).toString('base64'))
const seed = mod.circuitSeed

const publicSeed = {
  people: seed.people.filter((p) => p.id === EVAN),
  logs: seed.logs.filter((l) => l.personId === EVAN),
  // keep movies Evan rated, exposing only his rating
  movies: seed.movies
    .filter((m) => m.ratings[EVAN] != null)
    .map((m) => ({ ...m, ratings: { [EVAN]: m.ratings[EVAN] } })),
  // watchlist titles are shared suggestions; show only Evan's votes
  watchlist: seed.watchlist.map((w) => ({
    ...w,
    votes: (w.votes ?? []).filter((v) => v === EVAN),
  })),
}

const out = `// AUTO-GENERATED by scripts/gen-public-seed.mjs — do not edit by hand.
// Evan-only (pid '${EVAN}') slice of the Circuit, derived from seed.ts at build-authoring
// time. This is the public demo data shown to signed-out visitors. Crucially it does NOT
// import seed.ts, so the rest of the group's real data never reaches the client bundle —
// the full group lives only in Supabase. Regenerate with: node scripts/gen-public-seed.mjs
import type { CircuitState } from './types'

export const publicSeed: CircuitState = ${JSON.stringify(publicSeed, null, 2)}
`

writeFileSync('src/circuit/publicSeed.ts', out)
console.log(
  `wrote publicSeed.ts — ${publicSeed.people.length} person, ${publicSeed.logs.length} logs, ` +
    `${publicSeed.movies.length} movies, ${publicSeed.watchlist.length} watchlist`,
)
